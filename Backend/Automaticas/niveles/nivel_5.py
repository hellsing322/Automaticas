
#Definir cual es el umbrar de diferencia entre estaciones correlacioandas   
#  NIVEL 5. VALIDACIÓN DELA COHERENCIA ESPACIAL. CONTRASTE DE 
# LOS DATOS DE CADA ESTACIÓN CON DATOS DE OTRAS ESTACIONES
# En este nivel de validación se tiene en cuenta que los valores de una misma variable 
# medidos al mismo tiempo en estaciones correlacionadas no pueden diferir demasiado unos 
# de otros (Eischeid et al., 1995; Gandin, 1988; Hubbard, 2001; Wade, 1987). El llevar a la 
# práctica este test puede resultar más o menos complicado dependiendo de la densidad de 
# estaciones y de lo compleja que sea la orografía de la zona. Para llevar a cabo este test se 
# aplicarán, por ejemplo, técnicas de interpolación. Es suficiente con aplicar algún método 
# sencillo que permitan detectar los errores más groseros. Los errores más sutiles se pueden 
# descubrir por inspección visual. Para validar la coherencia espacial se suele estimar un 
# valor para cada observación. Esto se hace a partir de datos de la misma variable (que no 
# hayan sido etiquetados como no válidos en niveles previos), registrados en otras estaciones 
# correlacionadas con la variable que se está analizando. A continuación, se calculará alguna 
# expresión que de cuenta de la diferencia entre el valor medido y el valor estimado. Si esta 
# diferencia excediese un cierto umbral (p.e. dos veces la desviación típica de los datos 
# empleados en la estimación), se considerará que el dato analizado no ha superado este test.
